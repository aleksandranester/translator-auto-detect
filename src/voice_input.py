"""
Модуль пользовательского интерфейса.

Отвечает за взаимодействие пользователя с системой:
отображение элементов управления, полей ввода и вывода результата.
"""


class UserInterface:
    """
    Класс модуля пользовательского интерфейса переводчика.

    Управляет полем ввода исходного текста, выбором целевого языка
    и отображением результата перевода. Проектируется с учётом
    требований удобства использования и адаптивности.

    :author: Нестеренко А.Р.
    :version: 1.0.0

    Attributes:
        input_text (str): Текст, введённый пользователем.
        target_language (str): Код языка перевода по умолчанию.
    """

    def __init__(self) -> None:
        """
        Инициализация модуля интерфейса.

        Устанавливает пустое поле ввода и язык перевода по умолчанию.
        """
        #: Исходный текст, введённый пользователем
        self.input_text: str = ""
        #: Язык перевода по умолчанию
        self.target_language: str = "en"

    def get_user_input(self) -> str:
        """
        Получить текст, введённый пользователем.

        :return: Строка с текущим содержимым поля ввода.
        :rtype: str

        Пример использования::

            >>> ui = UserInterface()
            >>> ui.input_text = "Hello"
            >>> ui.get_user_input()
            'Hello'
        """
        return self.input_text

    def display_result(self, result: str) -> None:
        """
        Отобразить результат перевода в поле вывода.

        Выводит переведённый текст пользователю.
        В графической реализации обновляет соответствующий виджет.

        :param result: Переведённый текст для отображения.
        :type result: str
        :return: None

        Пример использования::

            >>> ui = UserInterface()
            >>> ui.display_result("Привет мир")
            Переведенный текст: Привет мир
        """
        print("Переведенный текст:", result)

    def clear_fields(self) -> None:
        """
        Очистить поля ввода и вывода интерфейса.

        Сбрасывает ``input_text`` в пустую строку.

        :return: None
        """
        self.input_text = ""
