"""
Модуль автоматического определения языка.

Определяет язык введённого пользователем текста на основе
анализа символьного состава без ручного выбора языка.
"""


def detect_language(text: str) -> str:
    """
    Определить язык переданного текста.

    Выполняет анализ символьного состава строки:
    при наличии кириллических символов возвращает ``"Русский"``,
    в остальных случаях — ``"Английский"``.

    :param text: Анализируемый текст.
    :type text: str
    :return: Название определённого языка (``"Русский"`` или ``"Английский"``).
    :rtype: str
    :raises TypeError: Если передан не строковый аргумент.

    Пример использования::

        >>> detect_language("Привет мир")
        'Русский'
        >>> detect_language("Hello world")
        'Английский'
    """
    if not isinstance(text, str):
        raise TypeError("Аргумент text должен быть строкой.")
    if any(char in text.lower() for char in "абвгдеёжзийклмнопрстуфхцчшщъыьэюя"):
        return "Русский"
    return "Английский"
